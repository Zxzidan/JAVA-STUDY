public class Arithmetic {
    public static void main(String[] args) {
        int a = 20;
        int b = 40;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        ++a;
        System.out.println(a);

        --b;
        System.out.println(b);
    }
}
